-- ---------------------------------------------------------------------
-- 1) Config de vigencia por programa (product_key reusa la nomenclatura existente)
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.program_config (
    product_key                  TEXT PRIMARY KEY,
    access_type                  TEXT NOT NULL CHECK (access_type IN ('subscription','fixed_days')),
    source_table                 TEXT NOT NULL CHECK (source_table IN ('subscriptions','sales')),
    requires_active_subscription BOOLEAN NOT NULL DEFAULT FALSE,
    valid_days                   INT,                 -- solo fixed_days
    requires_program_key         TEXT,                -- dependencia (infinity_vip -> infinity)
    valid_statuses               TEXT[] NOT NULL,     -- statuses que cuentan como vigente
    is_active                    BOOLEAN NOT NULL DEFAULT TRUE,
    sort_order                   INT NOT NULL DEFAULT 0,
    created_at                   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at                   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
DROP TRIGGER IF EXISTS trg_program_config_updated_at ON public.program_config;
CREATE TRIGGER trg_program_config_updated_at BEFORE UPDATE ON public.program_config
    FOR EACH ROW EXECUTE FUNCTION public.tg_set_updated_at();

