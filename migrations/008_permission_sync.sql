-- ---------------------------------------------------------------------
-- 2) Vigencias calculadas por usuario (auditoría / soporte)
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.user_program_validity (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id        BIGINT NOT NULL,
    email         TEXT NOT NULL,
    product_key   TEXT NOT NULL,
    is_valid      BOOLEAN NOT NULL,
    reason        TEXT,
    valid_until   TIMESTAMPTZ,
    source_status TEXT,
    computed_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_upv_run   ON public.user_program_validity (run_id);
CREATE INDEX IF NOT EXISTS idx_upv_email ON public.user_program_validity (LOWER(email));

-- ---------------------------------------------------------------------
-- 3) Corridas del sync de permisos
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.permission_sync_runs (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    started_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    finished_at      TIMESTAMPTZ,
    mode             TEXT NOT NULL DEFAULT 'apply' CHECK (mode IN ('apply','dry_run')),
    status           TEXT NOT NULL DEFAULT 'running'
                     CHECK (status IN ('running','success','partial','failed','aborted_locked')),
    users_processed  INT NOT NULL DEFAULT 0,
    users_changed    INT NOT NULL DEFAULT 0,
    grants_ok        INT NOT NULL DEFAULT 0,
    grants_failed    INT NOT NULL DEFAULT 0,
    revokes_ok       INT NOT NULL DEFAULT 0,
    revokes_failed   INT NOT NULL DEFAULT 0,
    accounts_created INT NOT NULL DEFAULT 0,
    accounts_missing INT NOT NULL DEFAULT 0,
    trigger_source   TEXT,
    message          TEXT
);
CREATE INDEX IF NOT EXISTS idx_psr_started ON public.permission_sync_runs (started_at DESC);

